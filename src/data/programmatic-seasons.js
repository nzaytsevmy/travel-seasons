export function getCountryMonthPaths() {
  const paths = [];
  for (const [countrySlug, country] of Object.entries(COUNTRIES)) {
    for (const monthKey of Object.keys(country.months)) {
      paths.push({
        params: { country: countrySlug, month: monthKey },
        props: {
          country,
          monthKey,
          monthMeta: MONTHS[monthKey],
          data: country.months[monthKey],
        },
      });
    }
  }
  return paths;
}
