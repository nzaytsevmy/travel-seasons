export function hasHolidays(slug) {
  return Boolean(HOLIDAYS[slug]?.length);
}

export function getHolidays(slug) {
  return HOLIDAYS[slug] || [];
}

// Сгруппировано по месяцам — для рендера в таблице/списке.
export function getHolidaysByMonth(slug) {
  const list = HOLIDAYS[slug] || [];
  const byMonth = {};
  for (const ev of list) {
    if (!byMonth[ev.month]) byMonth[ev.month] = [];
    byMonth[ev.month].push(ev);
  }
  return byMonth;
}

// Кросс-страновой срез: все события месяца (1-12) по ВСЕМ странам — для /events/<month>/.
// Каждый элемент помечен slug страны, чтобы линковать на её хаб.
export function getEventsByMonth(monthNum) {
  const out = [];
  for (const slug of Object.keys(HOLIDAYS)) {
    for (const ev of HOLIDAYS[slug]) {
      if (ev.month === monthNum) out.push({ slug, ...ev });
    }
  }
  return out;
}

// Топ событий года по значимости (closed > fest > culture) — для годового хаба /events/.
// Дедуп по названию (одно событие в нескольких странах — напр. Чуньцзе в Китае и Хайнане)
// + максимум одно событие на страну, чтобы список не забивался одной страной.
export function getTopEventsOfYear(n = 8) {
  const TAG_WEIGHT = { closed: 3, fest: 2, culture: 1 };
  const norm = (s) => (s || '').toLowerCase().replace(/[^a-zа-я0-9]/gi, '');
  const all = [];
  for (const slug of Object.keys(HOLIDAYS)) {
    for (const ev of HOLIDAYS[slug]) all.push({ slug, ...ev });
  }
  const sorted = all.sort((a, b) =>
    (TAG_WEIGHT[b.tag] || 0) - (TAG_WEIGHT[a.tag] || 0) || a.month - b.month
  );
  const seenName = new Set();
  const seenCountry = new Set();
  const out = [];
  for (const ev of sorted) {
    const nameKey = norm(ev.name);
    if (seenName.has(nameKey) || seenCountry.has(ev.slug)) continue;
    seenName.add(nameKey);
    seenCountry.add(ev.slug);
    out.push(ev);
    if (out.length >= n) break;
  }
  return out;
}
